    /* 获取时间区间 */
    function getDatePeriod($period){
    	$year = date( 'Y' );
		$month = date('m');
		$day = date('d');
		$periodMap = array(
				'yesterday' => array('start_date'=>date('Y-m-d', strtotime('-1 days')), 'end_date'=>date('Y-m-d', strtotime('-1 days'))),
				'last_yesterday' => array('start_date'=>date('Y-m-d', strtotime('-2 days')), 'end_date'=>date('Y-m-d', strtotime('-2 days'))),
				'current_month'=>array('start_date'=>date('Y-m-01'),'end_date'=>date('Y-m-d')),
				'last_current_month'=>array('start_date'=>date('Y-m-01',strtotime('-1 month')),'end_date'=>date('Y-m-t', strtotime('last month'))),
				'current_week'=>array('start_date'=>date('Y-m-d ', mktime(0, 0, 0, $month, $day - date('N'), $year)),'end_date'=>date('Y-m-d',time())),
				'last_current_week'=>array('start_date'=>date('Y-m-d ', mktime(0, 0, 0, $month, $day - date('N')-7, $year)),'end_date'=>date('Y-m-d ', mktime(0, 0, 0, $month, $day - date('N')-1, $year))),
				'seven_days'=>array('start_date'=>date('Y-m-d',strtotime('-7 days')),'end_date'=>date('Y-m-d',strtotime('-1 days'))),
				'last_seven_days'=>array('start_date'=>date('Y-m-d',strtotime('-14 days')),'end_date'=>date('Y-m-d',strtotime('-8 days'))),
				'thirty_days'=>array('start_date'=>date('Y-m-d',strtotime('-30 days')),'end_date'=>date('Y-m-d',strtotime('-1 days'))),
				'last_thirty_days'=>array('start_date'=>date('Y-m-d',strtotime('-60 days')),'end_date'=>date('Y-m-d',strtotime('-31 days'))),
				'one_year'=>array('start_date'=>date('Y-m-d',strtotime('-365 days')),'end_date'=>date('Y-m-d',strtotime('-1 days'))),
				'last_one_year'=>array('start_date'=>date('Y-m-d',strtotime('-730 days')),'end_date'=>date('Y-m-d',strtotime('-366 days')))
		);
    	return $periodMap[$period];
    }
    
    /* 获取环比时间段 */
    function getPrevious($startTime, $endTime){
    	return array(
    			'endDate' => date('Y-m-d', strtotime($startTime)-86400).' 23:59:59',
    			'startDate' => date('Y-m-d', strtotime($startTime)-(strtotime($endTime)-strtotime($startTime))).' 00:00:00'
    	);
    }
    
    /* 获取单例mongo连接句柄 */
    function getMongoClient($connection='') {
    	require_once "third_party".DS."MongoConnector.class.php";
