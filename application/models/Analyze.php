    		$mongo_cfg = ZendX_Config::get("mongodb","mongodb");
    		$connection = "mongodb://{$mongo_cfg['host']}:{$mongo_cfg['port']}";
    	}
    	return MongoConnector::get_mongo_client($connection);
    }

